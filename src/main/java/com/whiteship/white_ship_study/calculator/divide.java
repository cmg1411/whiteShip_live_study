package com.whiteship.white_ship_study.calculator;

public class divide {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.println(a / b); // 3

        double c = 1.5;
        int d = 5;
        System.out.println(c / d); // 0.3
        System.out.println(d / c); // 3.333333..
    }
}
